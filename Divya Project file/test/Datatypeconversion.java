package test;

public class Datatypeconversion {

	public static void main(String[] args) {
		
		int a = 50;
		System.out.println("Int a = "+ a);
		
		int b = 2;
		System.out.println("Int multiply by = "+ b);
		
		int c = a*b;
		System.out.println("Total = "+ c);
		
		
		float a1 = (float) 45.8;
		System.out.println("Float a1 = "+ a1);
		
		float b1 = (float) 2.1;
		System.out.println("Float subtract by = "+ b1);
		
		float c1 = a1-b1;
		System.out.println("Total: "+ c1);
		
		
		long a2 = 5000;
		System.out.println("Addition = "+ a2);
		
		long b2 = 300;
		System.out.println("Addition by = "+ b2);
		
		long c2 = a2+b2;
		System.out.println("Total: "+ c2);
		

	}

}
