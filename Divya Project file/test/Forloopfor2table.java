package test;

public class Forloopfor2table {

	public static void main(String[] args) {
		
		
		System.out.println("2 Table");
		for(int i=1;i<=10;i=i+1) 
		{
			System.out.println(i+"x2="+ i*2);
		}
		
		System.out.println("\n4 Table");
		for(int i=1;i<=10;i=i+1) 
		{
			System.out.println(i+"x4="+ i*4);
		}

	}

}
